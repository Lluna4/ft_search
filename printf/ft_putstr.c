#include "ft_printf.h"

int	ft_putstr(char *s)
{
	int	ret;

	ret = 0;
	if (!s)
		s = "(null)";
	while (*s)
	{
		ret++;
		if (ft_putchar(*s) == -1)
			return (-1);
		s++;
	}
	return (ret);
}
